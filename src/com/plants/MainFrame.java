    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed

        Plant plant = new Plant();
        plant = this.getSelectedPlant();
        if(plant==null){
            JOptionPane.showMessageDialog(null,"Please Select any row","Empty Selection",JOptionPane.INFORMATION_MESSAGE);
        }
        else{
            int confirm = JOptionPane.showConfirmDialog(null, "Are you Sure to Delete ?","Confirm Deletion",JOptionPane.INFORMATION_MESSAGE);
            if(confirm==0){
                String message = "";
                PlantDeleteService PDService = new PlantDeleteService();
                message = PDService.deletePlant(plant);
                if(message.equals("success")){
                    this.displayDataInTable();
                    this.displayCommonNameList();
                    JOptionPane.showMessageDialog(null,"Deleted Successfully..!!","Success",JOptionPane.INFORMATION_MESSAGE);
                }
                else{
                    JOptionPane.showMessageDialog(null,message,"Failed",JOptionPane.INFORMATION_MESSAGE);
                }
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void searchItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchItemActionPerformed
       String comName;
       comName = itemList.getSelectedItem().toString();
       if(comName.equals("")){
           JOptionPane.showMessageDialog(null,"Please Type Name for searching.!!","Empty Field",JOptionPane.INFORMATION_MESSAGE);
       }
       else{
           PlantSearchService PSService = new PlantSearchService();
           Plant plant = PSService.searchPlant(comName);
           if(plant ==null){
               JOptionPane.showMessageDialog(null,"Connection Problem try restarting your server","Connection Problem",JOptionPane.ERROR_MESSAGE);
           }
           else{
               if(plant.getGenus().equals("true")){
                   JOptionPane.showMessageDialog(null,"Search Not Found ...!!!","Search Not Found",JOptionPane.INFORMATION_MESSAGE);
               }
               else{
                   SinglePlantView singlePlant = new SinglePlantView();
                   singlePlant.setVisible(true);
                   singlePlant.setPlant(plant);
               }
           }
       }
    }//GEN-LAST:event_searchItemActionPerformed

