    private Plant getSelectedPlant(){
        Plant plant = new Plant();
        try{
            int rowIndex = displayTable.getSelectedRow();
            TableModel model = displayTable.getModel();
            String commonNamee = model.getValueAt(rowIndex,1).toString();
            for(int i=0;i<this.plants.size();i++){
            if(this.plants.get(i).getCommonName().equals(commonNamee)){
                plant = this.plants.get(i);
            }
        }
        }catch(Exception er){
            plant = null;
        }
        
        return plant;
    }
